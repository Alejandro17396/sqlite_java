        // TODO add your handling code here:
        if(startLevelTextField.getText().isEmpty() || endLevelTextField.getText().isEmpty()){
            
        }else{
            
        
        start=m.getLevel(startLevelTextField.getText());
        end=m.getLevel(endLevelTextField.getText());
        start1=m.getConcretWing(startLevelTextField.getText());
        end1=m.getConcretWing(endLevelTextField.getText());
        if(start1!=null && end1!=null){
             long totalExp=0;
            for(int i=start;i<end;i++){
                totalExp+=m.getData().get(i).getExp();
            }
            int ini;
            if(initialExplTextField.getText().isEmpty()){
                ini=0;
            }else{
                ini=Integer.parseInt(initialExplTextField.getText());
            }
            int colorfulPlume=(int) ((totalExp-ini)/20);
            totalExpLabel.setText("Totatl exp needed "+(totalExp-ini));
            colorfulPlumeLabel.setText("Colorful plume : "+colorfulPlume);
            statsLabel1.setText("<html>"+setStatsLabel(start1));
            statsLabel.setText("<html>"+setStatsLabel(end1));
            bonusStatsLabel.setText("<html>"+createBonusLabel(start1,end1));
        }
       
        
        }
        

