    private void TableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TableMouseClicked

        DefaultTableModel modelo = (DefaultTableModel) Table.getModel();
        int fila=Table.getSelectedRow();
        Wings aux= m.getData().get(fila);

        if(start==-1 || fila<start){
            start1=aux;
            start=fila;
            startLevelTextField.setText(start1.getName());
            statsLabel1.setText("<html>"+setStatsLabel(aux));
        }else if(end==-1 || fila>end ){
            end1=aux;
            end=fila;
            endLevelTextField.setText(end1.getName());
            statsLabel.setText("<html>"+setStatsLabel(aux));
        }else{
            start1=aux;
            start=fila;
            startLevelTextField.setText(start1.getName());
            statsLabel1.setText("<html>"+setStatsLabel(aux));
        }
        
        if(start!=-1 && end!=-1){
            
            bonusStatsLabel.setText("<html>"+createBonusLabel(start1,end1));
        }
    }//GEN-LAST:event_TableMouseClicked

    private String createBonusLabel(Wings start1, Wings end1) {
        
        Wings aux=new Wings();
        aux.setForce(end1.getForce()-start1.getForce());
        aux.setChakra(end1.getChakra()-start1.getChakra());
        aux.setAgi(end1.getAgi()-start1.getAgi());
        aux.setPower(end1.getPower()-start1.getPower());
        int dr=Integer.parseInt(end1.getDamageRate().replace("%", ""))-Integer.parseInt(start1.getDamageRate().replace("%", ""));
        int ai=Integer.parseInt(end1.getAvoidInjury().replace("%", ""))-Integer.parseInt(start1.getAvoidInjury().replace("%", ""));
        aux.setDamageRate(dr+"%");
        aux.setAvoidInjury(ai+"%");
        System.out.println(aux.toString());
        
        String text="";
        text+="Force        : "+aux.getForce()+"<br>";
        text+="Chakra       : "+aux.getChakra()+"<br>";
        text+="Agi          : "+aux.getAgi()+"<br>";
        text+="Power        : "+aux.getPower()+"<br>";
        text+="Damage rate  : "+aux.getDamageRate()+"<br>";
        text+="Avoid injury : "+aux.getAvoidInjury()+"<br>";
        return text; 
       
        
    }
    
