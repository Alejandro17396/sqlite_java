    private void setLevels() {
        String [][] table=createTable();
        String [] titulos= new String []{
          "Level","Exp level",
        };
        
        for(String s :titulos){
              marco.addColumn(s);
        }
        for(String [] s : table){
            marco.addRow(s);
        }
    }
    private String [][] createTable(){
        String matriz[][]=new String[m.getData().size()][2];
       
        ArrayList <Wings> aux=m.getData();
        for(int i=0;i<aux.size();i++){
            
            matriz[i][0]=aux.get(i).getName();
            matriz[i][1]=String.format("%d", aux.get(i).getExp());
          
            
        }
        return matriz;
    }

    









